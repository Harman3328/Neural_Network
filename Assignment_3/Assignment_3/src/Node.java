public class Node {
    double s;
    double a;
    double DO;
    double DI;

    public void setS(double s) {
        this.s = s;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getS() {
        return s;
    }

    public double getA() {
        return a;
    }

    public void setDO(double DO) {
        this.DO = DO;
    }

    public void setDI(double DI) {
        this.DI = DI;
    }

    public double getDO() {
        return DO;
    }

    public double getDI() {
        return DI;
    }
}
